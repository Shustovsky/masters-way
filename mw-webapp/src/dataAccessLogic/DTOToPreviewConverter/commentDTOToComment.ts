import {SchemasCommentPopulatedResponse} from "src/apiAutogenerated/general";
import {Comment} from "src/model/businessModel/Comment";

/**
 * Convert {@link CommentDTO} to {@link Comment}
 */
export const commentDTOToComment = (commentDTO: SchemasCommentPopulatedResponse): Comment => {
  return new Comment({
    ...commentDTO,
    createdAt: new Date(commentDTO.createdAt),
    updatedAt: new Date(commentDTO.updatedAt),
  });
};
