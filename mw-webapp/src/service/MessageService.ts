import {CreateMessageRequest, SchemasMessageResponse, UpdateMessageStatusRequest} from "src/apiAutogenerated/chat";
import {messageService} from "src/service/services";

/**
 * Provides methods to interact with the room's message
 */
export class MessageService {

  /**
   * Create message in chat room
   */
  public static async createMessageInRoom(requestParameters: CreateMessageRequest): Promise<SchemasMessageResponse> {
    const message = await messageService.createMessage(requestParameters);

    return message;
  }

  /**
   * Mark message as read
   */
  public static async updateMessageStatus(requestParameters: UpdateMessageStatusRequest): Promise<void> {
    await messageService.updateMessageStatus(requestParameters);
  }

}
