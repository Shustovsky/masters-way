import {
  CreateDayReportRequest,
  GetDayReportsRequest,
  SchemasCompositeDayReportPopulatedResponse,
  SchemasListDayReportsResponse,
} from "src/apiAutogenerated/general";
import {dayReportService} from "src/service/services";

/**
 * Provides methods to interact with the DayReports
 */
export class DayReportService {

  /**
   * Create day report
   */
  public static async createDayReport(
    requestParameters: CreateDayReportRequest,
  ): Promise<SchemasCompositeDayReportPopulatedResponse> {
    const dayReport = await dayReportService.createDayReport(requestParameters);

    return dayReport;
  }

  /**
   * Get day reports
   */
  public static async getDayReports(requestParameters: GetDayReportsRequest): Promise<SchemasListDayReportsResponse> {
    const dayReport = await dayReportService.getDayReports(requestParameters);

    return dayReport;
  }

}
